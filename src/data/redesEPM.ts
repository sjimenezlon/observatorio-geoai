// Segmentos de red de infraestructura sobre vías reales del Valle de Aburrá y Rionegro
// Trazados siguiendo corredores viales reales con múltiples vértices
import type { FeatureCollection, Feature, LineString } from "geojson";

export interface SegmentProperties {
  id: string;
  tipo_red: string;
  cobertura: "zona_verde" | "via_pavimentada" | "via_destapada" | "bajo_construccion";
  confianza: number;
  ndvi: number;
  ndbi: number;
  bsi: number;
  fuente: string;
  municipio: string;
  longitud_m: number;
  color: string;
}

const COLOR_MAP: Record<string, string> = {
  zona_verde: "#22c55e",
  via_pavimentada: "#6b7280",
  via_destapada: "#d97706",
  bajo_construccion: "#ef4444",
};

function seg(
  id: string,
  coords: [number, number][],
  tipo_red: string,
  cobertura: SegmentProperties["cobertura"],
  municipio: string,
  confianza: number,
  ndvi: number,
  ndbi: number,
  bsi: number
): Feature<LineString, SegmentProperties> {
  // Calcular longitud aproximada sumando distancias entre puntos
  let dist = 0;
  for (let i = 1; i < coords.length; i++) {
    const dx = (coords[i][0] - coords[i - 1][0]) * 111320 * Math.cos((coords[i][1] * Math.PI) / 180);
    const dy = (coords[i][1] - coords[i - 1][1]) * 110540;
    dist += Math.sqrt(dx * dx + dy * dy);
  }
  return {
    type: "Feature",
    properties: {
      id,
      tipo_red,
      cobertura,
      confianza,
      ndvi: Math.round(ndvi * 1000) / 1000,
      ndbi: Math.round(ndbi * 1000) / 1000,
      bsi: Math.round(bsi * 1000) / 1000,
      fuente: confianza > 0.85 ? "dynamic_world+osm" : confianza > 0.7 ? "dynamic_world+indices" : "indices",
      municipio,
      longitud_m: Math.round(dist),
      color: COLOR_MAP[cobertura],
    },
    geometry: { type: "LineString", coordinates: coords },
  };
}

